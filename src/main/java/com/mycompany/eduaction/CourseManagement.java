    private void jButton3ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton3ActionPerformed
        int i = 0;
        Course c1 = new Course();
        c1.setId(Integer.parseInt(jTextField1.getText()));
        c1.setName(name2.getText());
        c1.setCategory(cat2.getText());
        c1.deleteCourse();
        while(jTable1.getValueAt(i, 0)!=null)
        {
            jTable1.setValueAt("", i, 0);
            jTable1.setValueAt("", i, 1);
            jTable1.setValueAt("", i, 2);

            i++;
        }
        DatabaseConnection db = new DatabaseConnection();
        db.getTableDataCourse(this);
        db.getTableDataCoursess(this);
        this.validate();
    }//GEN-LAST:event_jButton3ActionPerformed

