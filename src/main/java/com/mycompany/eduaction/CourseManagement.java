    private void jButton1ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton1ActionPerformed
        name_type = true;
        char[] cn = name2.getText().toCharArray();
        char[] cn1 = cat2.getText().toCharArray();
        for (char c : cn) 
          {
                if (Character.isDigit(c)) 
                {
                    name_type = false;
                    JOptionPane.showMessageDialog(rootPane, "Invalid Name");
                    break;
                }
                
          }
          for (char c : cn1) 
          {
                if (Character.isDigit(c)) 
                {
                    name_type = false;
                    JOptionPane.showMessageDialog(rootPane, "Invalid Category");
                    break;
                }
                
          }
        while(name_type != false)
        {
        Course c1 = new Course();
        c1.setId(Integer.parseInt(jTextField1.getText()));
        c1.setName(name2.getText());
        c1.setCategory(cat2.getText());
        c1.addCourse();
        DatabaseConnection db = new DatabaseConnection();
        db.getTableDataCourse(this);
        db.getTableDataCoursess(this);
        name_type = false;
        break;   
        }
    }//GEN-LAST:event_jButton1ActionPerformed

