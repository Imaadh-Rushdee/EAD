    private void jButton2ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton2ActionPerformed
        name_type = true;
        char[] cn = name.getText().toCharArray();  
        for (char c : cn) 
          {
                if (Character.isDigit(c)) 
                {
                    name_type = false;
                    JOptionPane.showMessageDialog(rootPane,"Invalid username");
                    break;
                }
                
          }
        
        while(name_type != false)
        {
                User u1 = new User();
                u1.setID(Integer.parseInt(id.getText()));
                u1.setName(name.getText());
                u1.setType(type.getSelectedItem().toString());
                u1.setPassword(pass.getText());
                u1.addUser();
                DatabaseConnection db = new DatabaseConnection();
                db.getUserData(this);
                db.getTableUserID(this);
                name.setText("");
                pass.setText("");
                type.setSelectedItem(1);
                
                name_type = false;
                break;
        }
                
          

    }//GEN-LAST:event_jButton2ActionPerformed

