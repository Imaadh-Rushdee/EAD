    private void jButton4ActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_jButton4ActionPerformed
        int i=0;
        User u1 = new User();
        u1.setID(Integer.parseInt(id.getText()));
        u1.deleteUser();
        while(tab_user.getValueAt(i, 0)!= null)
        {
            tab_user.setValueAt("", i, 0);
            tab_user.setValueAt("", i, 1);
            tab_user.setValueAt("", i, 2);
            tab_user.setValueAt("", i, 3);
            i++;
        }
        DatabaseConnection db = new DatabaseConnection();
        db.getUserData(this);
        db.getTableUserID(this);
        name.setText("");
        pass.setText("");
        type.setSelectedItem(1);
    }//GEN-LAST:event_jButton4ActionPerformed

    private void tab_userMouseClicked(java.awt.event.MouseEvent evt) {//GEN-FIRST:event_tab_userMouseClicked
       if(tab_user.getValueAt(tab_user.getSelectedRow(), tab_user.getSelectedColumn())!=null)
       {
        int row = tab_user.getSelectedRow();
        this.name.setText(tab_user.getValueAt(row, 1).toString());
        this.type.setSelectedItem(tab_user.getValueAt(row, 2).toString());
        this.pass.setText(tab_user.getValueAt(row, 3).toString());
        this.id.setText(tab_user.getValueAt(row, 0).toString());
       }
       else
       {
           
        DatabaseConnection db = new DatabaseConnection();
        db.getTableUserID(this);
        name.setText("");
        pass.setText("");
        type.setSelectedItem(1);
       }
    }//GEN-LAST:event_tab_userMouseClicked

